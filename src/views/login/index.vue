<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" auto-complete="on"
      label-position="left">
      <h3 class="title">我的博客</h3>
      <el-form-item prop="username">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input v-model="loginForm.username" name="username" type="text" auto-complete="on" placeholder="用户名" />
      </el-form-item>
      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input :type="pwdType" v-model="loginForm.password" name="password" auto-complete="on" placeholder="密码" />
        <span class="show-pwd" @click="showPwd">
          <svg-icon :icon-class="pwdType === 'password' ? 'eye' : 'eye-open'" />
        </span>
      </el-form-item>
      <el-form-item>
        <el-button :loading="loading" type="primary" style="width:100%;" @click.native.prevent="login()">
          登录
        </el-button>
      </el-form-item>
      <div class="tips">
        <span style="margin-right:20px;">username: admin</span>
        <span> password: admin</span>
      </div>
    </el-form>
  </div>
</template>

<script>
  import {
    isvalidUsername
  } from '@/utils/validate'
  import apipath from '../../assets/js/api'
  import axios from 'axios'
  import {
    isNotEmpty,
    isChinese
  } from '../../common/commonValidate'
  export default {
    name: 'Login',
    data() {
      const validateUsername = (rule, value, callback) => {
        if (!isvalidUsername(value)) {
          callback(new Error('请输入正确的用户名'))
        } else {
          callback()
        }
      }
      const validatePass = (rule, value, callback) => {
        if (value.length < 5) {
          callback(new Error('密码不能小于5位'))
        } else {
          callback()
        }
      }
      return {
        loginForm: {
          username: 'admin',
          password: 'admin',

        },
        loginRules: {
          username: [{
            required: true,
            trigger: 'blur',
            validator: validateUsername
          }],
          password: [{
            required: true,
            trigger: 'blur',
            validator: validatePass
          }]
        },
        loading: false,
        pwdType: 'password',
        redirect: undefined,
        tip: '',
      }
    },
    watch: {
      $route: {
        handler: function (route) {
          this.redirect = route.query && route.query.redirect
        },
        immediate: true
      }
    },
    methods: {
      showPwd() {
        if (this.pwdType === 'password') {
          this.pwdType = ''
        } else {
          this.pwdType = 'password'
        }
      },
      validateAll() {
        let flat = false;
        if (!isNotEmpty(this.loginForm.username)) {
          flat = false;
          this.tip = "用户名不能为空";
          return;
        } else {
          this.tip = " ";
          flat = true;
        }

        if (!isNotEmpty(this.loginForm.password)) {
          flat = false;
          this.tip = "密码不能为空";
          return;
        } else {
          flat = true;
          this.tip = " ";
        }
        if (this.loginForm.password.length < 5 || this.loginForm.password.length > 12) {
          flat = false;
          this.tip = "密码长度不能小于5大于11";
          return;
        } else {
          this.tip = " ";
          flat = true;
        }

        return flat
      },
      login() {
        if (!this.validateAll()) {
          console.log('验证失败');
          return;
        }
        localStorage.setItem("username",this.loginForm.username);
        localStorage.setItem("password",this.loginForm.password);
        this.loading = true
        axios({
          method: 'POST',
          url: apipath.LOGIN,
          data: {
            username: this.loginForm.username,
            password: this.loginForm.password
          },
        }).then(res => {
          console.log(res);

          console.log('fdsfdf');
          // this.loading = false
          //   this.$store.dispatch('Login', this.loginForm).then(() => {
          //     this.loading = false
          //     this.$router.push({
          //       path: this.redirect || '/'
          //     })
          //   }).catch(() => {
          //     this.loading = false
          //   })
        }).catch(err => {
          console.log('接口请求失败');
          return false
        });
      },
    }
  }

</script>

